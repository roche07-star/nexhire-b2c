/**
 * 이력서 생성 프롬프트
 * 분석 결과 기반 개선 이력서 HTML 생성
 */

export function buildGenerateResumePrompt(params: {
  originalText: string
  analysisResult: any
}): string {
  const { originalText, analysisResult } = params

  return `당신은 10년 경력의 한국 시니어 헤드헌터입니다.
구직자의 이력서를 분석한 결과를 바탕으로 **채용 담당자가 보기 좋은 개선 이력서**를 HTML 형식으로 작성합니다.

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
📋 원본 이력서
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
${originalText}

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
📊 분석 결과
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
• 이름: ${analysisResult.candidate_name || '[이름]'}
• 직무: ${analysisResult.job_title || '미지정'}
• 경력: ${analysisResult.experience_years || 0}년
• 핵심 키워드: ${(analysisResult.keywords || []).join(', ')}
• 강점: ${(analysisResult.strengths || []).join(' / ')}
• 개선 포인트: ${(analysisResult.improvements || []).join(' / ')}
• 종합 요약: ${analysisResult.summary || ''}

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
🎯 작성 원칙 (절대 준수)
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

**1. 원본 정보 100% 정확히 추출 (최우선!)**

❗❗❗ **원본 이력서를 한 줄 한 줄 꼼꼼히 읽고, 아래 정보를 정확히 추출하십시오** ❗❗❗

**📌 학력 (Education) - 원본에서 정확히 추출!**
- ✅ 대학명: "서울대학교", "연세대학교" 등 **원본 그대로**
- ✅ 전공: "컴퓨터공학과", "경영학과" 등 **원본 그대로**
- ✅ 졸업일/재학기간: "2015.03 ~ 2019.02" 등 **원본 그대로**
- ✅ 학위: "학사", "석사" 등 (원본에 있으면)
- ❌ 절대 금지: "[대학교]", "[전공]" 플레이스홀더 사용
- ❌ 절대 금지: "주요 대학교 졸업" 같은 일반화

**📌 경력 (Work Experience) - 원본에서 정확히 추출!**
- ✅ 회사명: "네이버", "카카오", "삼성전자" 등 **원본 그대로** (약어/정식명칭 원본 따름)
- ✅ 직책/직급: "선임 연구원", "책임 개발자" 등 **원본 그대로**
- ✅ 재직기간: "2020.03 ~ 2023.12 (3년 10개월)" **정확한 날짜와 기간**
  * 원본에 날짜가 있으면 **반드시 월까지 표기**
  * 기간 계산: 2020.03 ~ 2023.12 = 3년 10개월 (정확히 계산!)
- ✅ 총 경력: 분석 결과의 total_experience_years 사용 (예: "총 8년 6개월")
  * 8.5년 = 8년 6개월
  * 3.2년 = 3년 2개월
  * **연차를 "연/개월"로 정확히 변환**
- ❌ 절대 금지: "[회사명]", "○○ 회사" 플레이스홀더
- ❌ 절대 금지: 재직기간을 "2020 ~ 2023"처럼 연도만 표기 (월까지 표기!)
- ❌ 절대 금지: 기간 계산 오류 (3년 10개월을 4년으로 표기)

**📌 기타 정보 - 원본에서 정확히 추출!**
- ✅ 프로젝트명: 원본에 언급된 **정확한 이름** (약어 포함)
- ✅ 자격증: 취득일, 발급기관 포함 (원본 그대로)
- ✅ 어학: 점수, 취득일 포함 (원본 그대로)
- ✅ 정량 수치: 매출, 인원, 처리량 등 **숫자 정확히**

❌ **절대 금지 사항:**
1. 원본에 있는 정보를 플레이스홀더로 대체
2. 원본에 명시된 정보를 누락
3. 없는 성과/경험 창작
4. 날짜/기간 대충 표기 (월까지 정확히!)
5. 회사명/학교명 일반화 ("대기업", "주요 대학")

✅ **작성 원칙:**
- 원본에 있는 정보 → **100% 정확히 그대로** 사용
- 원본에 없는 정보 → 플레이스홀더 또는 생략 (창작 금지)
- 애매한 정보 → 원본 문구 그대로 사용 (해석/변형 금지)

**2. 분석 결과 적극 반영**
✅ **강점 부각**:
${(analysisResult.strengths || []).map((s: string, i: number) => `  ${i + 1}. ${s} → 구체적 수치/프로젝트명과 함께 서술`).join('\n')}

✅ **개선 포인트 적극 보완** (매우 중요!):
${(analysisResult.improvements || []).map((imp: string, i: number) => `  ${i + 1}. ${imp}
     → 원본 이력서에서 관련 경험을 찾아 더 구체적으로 서술
     → 부족한 부분은 관련 경험으로 간접 보완 (예: "○○ 프로젝트에서 △△ 경험 축적")
     → 없는 내용 창작 금지, 있는 것을 최대한 긍정적으로 재해석`).join('\n')}

**3. 작성 지침**
• 상투어 제거: "열정적", "탁월한", "소통왕" 등 → 구체적 협업 대상과 역할로 대체
• 단순 나열("~담당", "~수행") → 역할과 결과가 보이는 서술로 교체
  예) "A/S 대응" → "고객사 A/S 대응 및 재발 방지 조치 수행 (클레임 30% 감소)"
• 핵심 키워드를 자연스럽게 녹여냄: ${(analysisResult.keywords || []).join(', ')}
• 문장 끝 명사형 통일: "~담당", "~수행", "~구현", "~운영"

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
📄 HTML 형식 요구사항
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

**필수 조건:**
1. **완전한 HTML 문서**: <!DOCTYPE html>부터 </html>까지
2. **인라인 CSS**: 모든 스타일은 <style> 태그 또는 인라인으로 (외부 CSS 없이)
3. **한국 기업용 레이아웃**: 깔끔하고 전문적인 디자인
4. **인쇄 최적화**: A4 용지 기준 (210mm × 297mm)
5. **반응형 디자인**: 모바일에서도 읽기 쉽게
6. **편집 가능**: contenteditable 속성 없이 (사용자가 직접 편집할 예정)

❗❗❗ **페이지 분량 (매우 중요!)** ❗❗❗
- 경력 3년 이상: 최소 2페이지 이상 작성
- 경력 5년 이상: 최소 2-3페이지 작성
- 경력 10년 이상: 최소 3-4페이지 작성
- **절대 1페이지로 압축 금지** - 내용을 충분히 상세하게 작성
- 각 경력 항목은 3-5줄 이상으로 상세 기술
- 프로젝트는 각각 2-3줄 이상으로 설명
- CSS에 page-break-inside: avoid 사용 (경력 항목 중간에 페이지 안 나뉨)

**포함할 섹션:**
1. **기본 정보** (상단 헤더)
   - ❗이름: 분석 결과의 candidate_name 사용 (있으면 반드시 표시!)
   - 연락처 (원본에서 추출, 없으면 [전화번호])
   - 이메일 (원본에서 추출, 없으면 [이메일])
   - ❗직무/포지션: 분석 결과의 job_title 사용 (반드시 표시!)

2. **경력 요약** (3-5줄)
   - 총 경력 연수
   - 주요 직무 경험
   - 핵심 성과 요약

3. **핵심 역량** (6-8개)
   - 분석 결과의 키워드 기반
   - 구체적 기술/도구명 포함

4. **경력 사항** (상세)
   - 회사명, 직책, 재직기간
   - 담당 업무 (불릿 포인트)
   - 주요 성과 (정량 수치 포함)

5. **학력**
   - 대학교, 전공, 졸업일

6. **프로젝트** (해당 시)
   - 프로젝트명, 기간
   - 역할, 기술 스택
   - 성과

7. **자격증 / 어학** (해당 시)

8. **푸터** (하단, 필수)
   - "JOBIZIC" 텍스트 (굵게, 14px)
   - "© 2026 Jobizic. All rights reserved." (회색, 12px)
   - 중앙 정렬, 상단 여백 40px

**디자인 가이드:**
- 글꼴: Noto Sans KR, 맑은 고딕, sans-serif
- 색상: 블랙(#000) 기본, 강조색(#2563eb) 섹션 제목
- 여백:
  * 데스크톱 (768px 이상): 상하좌우 40px, 섹션 간 24px
  * 모바일 (768px 미만): 상하좌우 12px, 섹션 간 16px (화면 최대한 넓게!)
- 폰트 크기: 이름 28px, 섹션 제목 20px, 본문 14px
- 줄 간격: 1.6
- 페이지 나누기: 경력 항목 사이에 page-break-inside: avoid 사용
- **레이아웃**: body에 max-width 설정 금지! 전체 화면 사용

**중요: 레이아웃 구조**
- ❌ position: absolute, fixed 절대 사용 금지
- ❌ float 사용 금지
- ✅ 모든 요소는 position: static 또는 relative만 사용
- ✅ 문서 흐름(document flow)에 따라 위에서 아래로 배치
- ✅ 섹션은 <section> 또는 <div>로 순차 배치
- ✅ 각 섹션은 margin-bottom으로 간격 조절

**주의사항:**
- 개인정보가 원본에 없다면 [이름], [이메일], [전화번호] 플레이스홀더 사용
- 과장하지 말고 사실 기반 작성
- 마크다운 코드 블록(\`\`\`html) 없이 순수 HTML만 출력

**HTML 구조 예시:**
\`\`\`html
<!DOCTYPE html>
<html>
<head>
  <meta charset="UTF-8">
  <meta name="viewport" content="width=device-width, initial-scale=1.0">
  <title>이력서 - [이름]</title>
  <style>
    * { box-sizing: border-box; }
    body {
      margin: 0;
      padding: 40px;
      font-family: 'Noto Sans KR', 'Malgun Gothic', sans-serif;
      line-height: 1.6;
      max-width: 100% !important; /* 전체 화면 사용 */
    }
    section { margin-bottom: 24px; position: relative; }
    h1 { font-size: 28px; margin: 0 0 8px 0; }
    h2 { color: #2563eb; font-size: 20px; margin-bottom: 12px; }

    /* 모바일 반응형: 화면 최대한 넓게! */
    @media (max-width: 768px) {
      body { padding: 12px !important; }
      section { margin-bottom: 16px; }
      h1 { font-size: 24px; }
      h2 { font-size: 18px; }
    }

    /* position: absolute 금지! */
  </style>
</head>
<body>
  <section class="header">
    <h1>[이름]</h1>
    <p>[직무] · [경력]년</p>
    ...
  </section>
  <section class="summary">...</section>
  <section class="skills">...</section>
  <section class="experience">...</section>
  <footer>JOBIZIC ...</footer>
</body>
</html>
\`\`\`

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

완성된 HTML을 바로 출력하십시오. 설명이나 주석 없이 HTML만 출력하십시오.`
}
