/**
 * 이력서 생성 프롬프트
 * 분석 결과 기반 개선 이력서 HTML 생성
 */

export function buildGenerateResumePrompt(params: {
  originalText: string
  analysisResult: any
}): string {
  const { originalText, analysisResult } = params

  return `당신은 10년 경력의 한국 시니어 헤드헌터입니다.
구직자의 이력서를 분석한 결과를 바탕으로 **채용 담당자가 보기 좋은 개선 이력서**를 HTML 형식으로 작성합니다.

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
📋 원본 이력서
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
${originalText}

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
📊 분석 결과
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
• 직무: ${analysisResult.job_title || '미지정'}
• 경력: ${analysisResult.experience_years || 0}년
• 핵심 키워드: ${(analysisResult.keywords || []).join(', ')}
• 강점: ${(analysisResult.strengths || []).join(' / ')}
• 개선 포인트: ${(analysisResult.improvements || []).join(' / ')}
• 종합 요약: ${analysisResult.summary || ''}

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
🎯 작성 원칙 (절대 준수)
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

**1. 사실 관계 절대 변경 금지**
- 회사명, 직책, 재직기간, 학력, 자격증 등 → 원본 그대로
- 없는 성과/경험 창작 금지 → 있는 것을 더 잘 보이게
- 정량 수치(매출, 인원, 처리량 등) → 원본 근거 있는 것만

**2. 분석 결과 적극 반영**
✅ **강점 부각**:
${(analysisResult.strengths || []).map((s: string, i: number) => `  ${i + 1}. ${s} → 구체적 수치/프로젝트명과 함께 서술`).join('\n')}

✅ **개선 포인트 적극 보완** (매우 중요!):
${(analysisResult.improvements || []).map((imp: string, i: number) => `  ${i + 1}. ${imp}
     → 원본 이력서에서 관련 경험을 찾아 더 구체적으로 서술
     → 부족한 부분은 관련 경험으로 간접 보완 (예: "○○ 프로젝트에서 △△ 경험 축적")
     → 없는 내용 창작 금지, 있는 것을 최대한 긍정적으로 재해석`).join('\n')}

**3. 작성 지침**
• 상투어 제거: "열정적", "탁월한", "소통왕" 등 → 구체적 협업 대상과 역할로 대체
• 단순 나열("~담당", "~수행") → 역할과 결과가 보이는 서술로 교체
  예) "A/S 대응" → "고객사 A/S 대응 및 재발 방지 조치 수행 (클레임 30% 감소)"
• 핵심 키워드를 자연스럽게 녹여냄: ${(analysisResult.keywords || []).join(', ')}
• 문장 끝 명사형 통일: "~담당", "~수행", "~구현", "~운영"

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
📄 HTML 형식 요구사항
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

**필수 조건:**
1. **완전한 HTML 문서**: <!DOCTYPE html>부터 </html>까지
2. **인라인 CSS**: 모든 스타일은 <style> 태그 또는 인라인으로 (외부 CSS 없이)
3. **한국 기업용 레이아웃**: 깔끔하고 전문적인 디자인
4. **인쇄 최적화**: A4 용지 기준 (210mm × 297mm)
5. **반응형 디자인**: 모바일에서도 읽기 쉽게
6. **편집 가능**: contenteditable 속성 없이 (사용자가 직접 편집할 예정)

**포함할 섹션:**
1. **기본 정보** (상단 헤더)
   - 이름 (없으면 [이름])
   - 연락처 (없으면 [전화번호])
   - 이메일 (없으면 [이메일])
   - 직무/포지션

2. **경력 요약** (3-5줄)
   - 총 경력 연수
   - 주요 직무 경험
   - 핵심 성과 요약

3. **핵심 역량** (6-8개)
   - 분석 결과의 키워드 기반
   - 구체적 기술/도구명 포함

4. **경력 사항** (상세)
   - 회사명, 직책, 재직기간
   - 담당 업무 (불릿 포인트)
   - 주요 성과 (정량 수치 포함)

5. **학력**
   - 대학교, 전공, 졸업일

6. **프로젝트** (해당 시)
   - 프로젝트명, 기간
   - 역할, 기술 스택
   - 성과

7. **자격증 / 어학** (해당 시)

8. **푸터** (하단, 필수)
   - "JOBIZIC" 텍스트 (굵게, 14px)
   - "© 2026 Jobizic. All rights reserved." (회색, 12px)
   - 중앙 정렬, 상단 여백 40px

**디자인 가이드:**
- 글꼴: Noto Sans KR, 맑은 고딕, sans-serif
- 색상: 블랙(#000) 기본, 강조색(#2563eb) 섹션 제목
- 여백: 상하좌우 40px, 섹션 간 24px
- 폰트 크기: 이름 28px, 섹션 제목 20px, 본문 14px
- 줄 간격: 1.6

**주의사항:**
- 개인정보가 원본에 없다면 [이름], [이메일], [전화번호] 플레이스홀더 사용
- 과장하지 말고 사실 기반 작성
- 마크다운 코드 블록(\`\`\`html) 없이 순수 HTML만 출력

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

완성된 HTML을 바로 출력하십시오. 설명이나 주석 없이 HTML만 출력하십시오.`
}
